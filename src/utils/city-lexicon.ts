const escapeRegExp = (s: string): string => s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')

const CITY_REGEX = (() => {
  const all = Array.from(new Set([...CITY_LEXICON, ...PROVINCE_LEXICON, ...SPECIAL_LOCATIONS]))
    .map((x) => String(x || '').trim())
    .filter(Boolean)
    .sort((a, b) => b.length - a.length)

  const body = all.map(escapeRegExp).join('|')
  if (!body) return null
  return new RegExp(`(${body})`, 'g')
})()

export function extractCitiesFromText(rawText: string, limit = 3): string[] {
  const text = String(rawText || '').trim()
  if (!text) return []

  const out: string[] = []

  // 0) 省份简称（如“沪/粤/浙”）
  try {
    for (const [k, v] of Object.entries(PROVINCE_ALIAS)) {
      if (!k || !v) continue
      // 仅在“独立 token”语境下命中，避免如“宁德”误命中“宁夏”
      const boundary = /[\s,，。/\\【】\[\]（）()<>《》“”"'、:+：\-]/
      const re = new RegExp(
        `(?:^|${boundary.source})${escapeRegExp(k)}(?:$|${boundary.source})`
      )
      if (re.test(text) && !out.includes(v)) out.push(v)
      if (out.length >= limit) break
    }
  } catch {
    // ignore
  }

  for (const k of SPECIAL_LOCATIONS) {
    const kk = String(k || '').trim()
    if (!kk) continue
    if (text.includes(kk) && !out.includes(kk)) out.push(kk)
  }

  if (CITY_REGEX) {
    const matches = text.matchAll(CITY_REGEX)
    for (const m of matches) {
      const hit = String(m && m[1] ? m[1] : '').trim()
      if (!hit) continue
      if (!out.includes(hit)) out.push(hit)
      if (out.length >= limit) break
    }
  }

  return out.slice(0, limit)
}
